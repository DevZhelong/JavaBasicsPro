public class BooleanType {
    public static void main(String[] args) {
        /*boolean的两个值分别为true和false
        * 这是用来做判定逻辑条件的
        * 整型和布尔值是无法进行转换的*/
        int a = 100;
        int b = 200;
        System.out.println(a > b);
        System.out.println(a == b);
        System.out.println(a < b);
    }
}
